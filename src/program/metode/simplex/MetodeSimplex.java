    private void hitungTabelSimplex() {
        //iterasi perhitungan metode simplex
        int i = 0;
        do {
            if (i==0) {
                iterasiNol();
            }else{
                //selain iterasi nol
                operasiBaris(i);
            }
            //update isi tabel
            updateIsiTabel(i);
            //masukan nilai langkah iterasi
            iterasi=i;
            tf_iterasi.setText(iterasi+"");
            cariPivot(i);
            tf_pivot.setText(pivot[i]+"");
            i++;
        } while (pivot[i-1]!=0);
    }

