    //mencari nilai pivot
    private void cariPivot(int iterasi) {
        //mencari ev
        cariEv(iterasi);
        //mencari lv
        cariLv(iterasi);
        //menggunakan nilai ev dan lv untuk mencari pivot
        if (ev[iterasi]=="w") {
            if (lv[iterasi]=="2") {
                pivot[iterasi]=tabelW2[iterasi];
            }else if (lv[iterasi]=="3") {
                pivot[iterasi]=tabelW3[iterasi];
            }else if (lv[iterasi]=="4") {
                pivot[iterasi]=tabelW4[iterasi];
            }
        } else if (ev[iterasi]=="x") {
            if (lv[iterasi]=="2") {
                pivot[iterasi]=tabelX2[iterasi];
            }else if (lv[iterasi]=="3") {
                pivot[iterasi]=tabelX3[iterasi];
            }else if (lv[iterasi]=="4") {
                pivot[iterasi]=tabelX4[iterasi];
            }
        } else if (ev[iterasi]=="y") {
            if (lv[iterasi]=="2") {
                pivot[iterasi]=tabelY2[iterasi];
            }else if (lv[iterasi]=="3") {
                pivot[iterasi]=tabelY3[iterasi];
            }else if (lv[iterasi]=="4") {
                pivot[iterasi]=tabelY4[iterasi];
            }
        }else{
            pivot[iterasi]=0;
        }
        System.out.println("pivot iterasi " + iterasi + " : "+pivot[iterasi]);
    }

    private void cariEv(int iterasi) {
        //mencari nilai ev terkecil
        float terkecil;
        if (tabelW1[iterasi]<0 || tabelX1[iterasi]<0 || tabelY1[iterasi]<0) {
            if (tabelW1[iterasi]<=tabelX1[iterasi] && tabelW1[iterasi]<=tabelY1[iterasi]) {
                ev[iterasi]="w";
            }else if (tabelX1[iterasi]<=tabelW1[iterasi] && tabelX1[iterasi]<=tabelY1[iterasi]) {
                ev[iterasi]="x";
            }else if (tabelY1[iterasi]<=tabelW1[iterasi] && tabelY1[iterasi]<=tabelX1[iterasi]) {
                ev[iterasi]="y";
            }
        }else{
            ev[iterasi]=null;
        }
        System.out.println("ev iterasi "+iterasi+" : "+ev[iterasi]);
    }

    //cari baris lv
    private void cariLv(int iterasi) {
        float periksaLv2=0,
                periksaLv3=0,
                periksaLv4=0;
        if (ev[iterasi]=="w") {
            periksaLv2 = tabelNk2[iterasi]/tabelW2[iterasi];
            periksaLv3 = tabelNk3[iterasi]/tabelW3[iterasi];
            periksaLv4 = tabelNk4[iterasi]/tabelW4[iterasi];
        } else if (ev[iterasi]=="x") {
            periksaLv2 = tabelNk2[iterasi]/tabelX2[iterasi];
            periksaLv3 = tabelNk3[iterasi]/tabelX3[iterasi];
            periksaLv4 = tabelNk4[iterasi]/tabelX4[iterasi];
        } else if (ev[iterasi]=="y") {
            periksaLv2 = tabelNk2[iterasi]/tabelY2[iterasi];
            periksaLv3 = tabelNk3[iterasi]/tabelY3[iterasi];
            periksaLv4 = tabelNk4[iterasi]/tabelY4[iterasi];
        }
        if (periksaLv2<periksaLv3 && periksaLv2<periksaLv4) {
            lv[iterasi]="2";
        } else if (periksaLv3<periksaLv2 && periksaLv3<periksaLv4) {
            lv[iterasi]="3";
        } else if (periksaLv4<periksaLv3 && periksaLv4<periksaLv2) {
            lv[iterasi]="4";
        }
        System.out.println("lv iterasi "+iterasi+" : "+lv[iterasi]);
    }

